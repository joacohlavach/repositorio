    private void btnLoginUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginUActionPerformed
        try{
            String userU = txtNombreU.getText();
            int tel = Integer.parseInt (txtTelU.getText());
            int edad = Integer.parseInt (txtEdadU.getText());
            
            if(edad < 18){
                JOptionPane.showMessageDialog(null, "Es necesario que usted sea mayor de edad");
            }else{
                Usuario user = new Usuario(userU, tel, edad);
                frmPedido pedidoU  = new frmPedido();
                pedidoU.setVisible(true);
                this.dispose();
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "datos incorrectos");
        }
    }//GEN-LAST:event_btnLoginUActionPerformed

